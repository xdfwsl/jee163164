package javasejs163164;

public class DuoChongIf {

	public static void main(String[] args) {

		int x = 99;

		if (x >= 90) {
			System.out.println("优");
		} else if (x >= 80) {
			System.out.println("良");
		} else if (x >= 70) {
			System.out.println("中");
		} else if (x >= 60) {
			System.out.println("及格");
		} else {
			System.out.println("不及格");
		}

	}

}
